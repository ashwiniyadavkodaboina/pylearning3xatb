#The ^ operator is a  bitwise exclusive or.
#It returns 1 if the bits are different, and 0 if they are the same.
#example: if 1 and 1 are the same, then 0 is returned.
#if 1 and 0 are the same, then 1 is returned.
a=int(input("enter the number:"))
b=int(input("enter the number:"))
print(f'The result is {a^b}')


